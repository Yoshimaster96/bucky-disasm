Enemy82Sub:
	;Check if boss is throwing bombs
	lda Enemy_SpriteLo+$01
	cmp #$1C
	beq Enemy82Sub_JT
	;Check if player is to left or right
	lda Enemy_X+$01
	cmp Enemy_X
	bcc Enemy82Sub_Left
	;Flip enemy X
	lda #$40
	bne Enemy82Sub_SetP
Enemy82Sub_Left:
	;Don't flip enemy X
	lda #$00
Enemy82Sub_SetP:
	sta Enemy_Props+$01
Enemy82Sub_JT:
	;Do jump table
	lda Enemy_Temp0+$01
	jsr DoJumpTable
Enemy82SubJumpTable:
	.dw Enemy82Sub_Sub0	;$00  Init
	.dw Enemy82Sub_Sub1	;$01  Move init
	.dw Enemy82Sub_Sub2	;$02  Move
	.dw Enemy82Sub_Sub3	;$03  Stop
;Sub 0: Init
Enemy82Sub_Sub0:
	;Set enemy position
	lda #$60
	sta Enemy_Y+$01
	lda #$00
	sta Enemy_X+$01
	;Spawn enemy
	sta $00
	lda #$14
	sta $01
	ldy #$02
	lda #$8D
	jsr SpawnEnemy
	;Set enemy X velocity
	lda #$04
	sta Enemy_XVelHi+$01
	sta Enemy_XVelHi+$02
	;Set enemy flags
	lda #(EF_ACTIVE|EF_PRIORITY|EF_HITBULLET|EF_HITENEMY|EF_VISIBLE)
	sta Enemy_Flags+$01
	lda #(EF_ACTIVE|EF_INVINCIBLE|EF_PRIORITY|EF_HITBULLET|EF_HITENEMY|EF_VISIBLE)
	sta Enemy_Flags+$02
	;Set enemy ID to enemy fire 2
	lda #ENEMY_ENEMYFIRE2
	sta Enemy_ID+$02
	;Next task ($01: Move init)
	inc Enemy_Temp0+$01
	;Clear sound
	jsr ClearSound
	;Play music
	ldy #MUSIC_BOSS2
	jmp LoadSound
;Sub 1: Move init
Enemy82Sub_Sub1:
	;If enemy X position not $60, exit early
	lda Enemy_X+$01
	cmp #$60
	bne Enemy82Sub_Sub1_Exit
Enemy82Sub_Sub1_Next:
	;Increment path data index
	inc Enemy_Temp6+$01
	lda Enemy_Temp6+$01
	cmp #$0C
	bne Enemy82Sub_Sub1_NoC
	lda #$00
	sta Enemy_Temp6+$01
Enemy82Sub_Sub1_NoC:
	;Get path segment velocity
	tay
	lda Level8BossXVel,y
	sta Enemy_XVelHi+$01
	sta Enemy_XVelHi+$02
	lda Level8BossYVel,y
	sta Enemy_YVelHi+$01
	sta Enemy_YVelHi+$02
	;Next task ($02: Move)
	inc Enemy_Temp0+$01
	;Set animation timer
	lda #$20
	sta Enemy_Temp2+$01
Enemy82Sub_Sub1_Exit:
	rts
;Sub 2: Move
Enemy82Sub_Sub2:
	;Update scroll
	jsr Level8BossUpdateScroll
	;Decrement animation timer, check if 0
	dec Enemy_Temp2+$01
	bne Enemy82Sub_Sub1_Exit
	;Set enemy X velocity
	lda #$03
	sta Enemy_XVelHi+$01
	sta Enemy_XVelHi+$02
	;Clear enemy Y velocity
	lda #$00
	sta Enemy_YVelHi+$01
	sta Enemy_YVelHi+$02
	;Reset animation timer
	lda #$20
	sta Enemy_Temp2+$01
	;Next task ($03: Stop)
	inc Enemy_Temp0+$01
	rts
;Sub 3: Stop
Enemy82Sub_Sub3:
	;Update scroll
	jsr Level8BossUpdateScroll
	;Decrement animation timer, check if 0
	dec Enemy_Temp2+$01
	beq Enemy82Sub_Sub3_Next
	;If animation timer $10, throw bomb
	lda Enemy_Temp2+$01
	cmp #$10
	beq Enemy82Sub_Sub3_Throw
	;If animation timer not $18, exit early
	cmp #$18
	bne Enemy82Sub_Sub1_Exit
	;Toggle throw bomb flag
	lda Enemy_Temp3+$01
	eor #$01
	sta Enemy_Temp3+$01
	;If not throwing bomb, exit early
	bne Enemy82Sub_Sub1_Exit
	;Set enemy flags
	lda #(EF_ACTIVE|EF_NOANIM|EF_PRIORITY|EF_HITBULLET|EF_HITENEMY|EF_VISIBLE)
	sta Enemy_Flags+$01
	;Save enemy sprite
	lda Enemy_SpriteLo+$01
	sta Enemy_Temp4+$01
	;Set enemy sprite
	lda #$1C
	sta Enemy_SpriteLo+$01
	rts
Enemy82Sub_Sub3_Throw:
	;If throw bomb flag set, exit early
	lda Enemy_Temp3+$01
	bne Enemy82Sub_Sub1_Exit
	;Set enemy flags
	lda #(EF_ACTIVE|EF_PRIORITY|EF_HITBULLET|EF_HITENEMY|EF_VISIBLE)
	sta Enemy_Flags+$01
	;Set enemy sprite
	lda Enemy_Temp4+$01
	sta Enemy_SpriteLo+$01
	;Spawn enemy
	lda #$00
	sta $00
	sta $01
	ldy #$03
	lda #$3C
	jsr SpawnEnemy
	;Set enemy ID to level 8 boss bomb
	lda #ENEMY_LEVEL8BOSSBOMB
	sta Enemy_ID+$03
	;Set enemy Y velocity/acceleration
	lda #$F8
	sta Enemy_YVelHi+$03
	lda #$7F
	sta Enemy_YAccel+$03
	;Check if player is to left or right
	lda Enemy_Props+$01
	beq Enemy82Sub_Sub3_Right
	;Set enemy X velocity left
	lda #$01
	bne Enemy82Sub_Sub3_SetX
Enemy82Sub_Sub3_Right:
	;Set enemy X velocity right
	lda #$05
Enemy82Sub_Sub3_SetX:
	sta Enemy_XVelHi+$03
	rts
Enemy82Sub_Sub3_Next:
	;Setup next task ($01: Move init)
	lda #$01
	sta Enemy_Temp0+$01
	jmp Enemy82Sub_Sub1_Next
Level8BossUpdateScroll:
	;If bits 0-1 of global timer not 0, exit early
	lda LevelGlobalTimer
	and #$03
	bne Level8BossUpdateScroll_Exit
	;If scroll X position $A8, exit early
	lda TempIRQBufferScrollX
	cmp #$A8
	beq Level8BossUpdateScroll_Exit
	;If scroll X position $FF, scroll left $0B
	cmp #$FF
	bne Level8BossUpdateScroll_NoXC
	sec
	sbc #$0A
	sta TempIRQBufferScrollX
Level8BossUpdateScroll_NoXC:
	;Scroll left $01
	dec TempIRQBufferScrollX
Level8BossUpdateScroll_Exit:
	rts
Level8BossXVel:
	.db $03,$05,$05,$01,$01,$03,$05,$05,$03,$03,$01,$01
Level8BossYVel:
	.db $FF,$FF,$01,$01,$FF,$FF,$01,$01,$FF,$FF,$01,$01

