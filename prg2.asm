RunGameSubmode_TitlePasswordSub:
	;Increment global timer
	inc LevelGlobalTimer
	;Check for UP press
	lda JoypadCur
	and #JOY_UP
	;Increment current character
	bne PasswordCharInc
	;Check for DOWN press
	lda JoypadCur
	and #JOY_DOWN
	beq RunGameSubmode_TitlePasswordSub_NoDec
	;Decrement current character
	jmp PasswordCharDec
RunGameSubmode_TitlePasswordSub_NoDec:
	;Check for A/RIGHT press
	lda JoypadDown
	and #(JOY_A|JOY_RIGHT)
	beq RunGameSubmode_TitlePasswordSub_NoApp
	;Append character
	jmp PasswordCharAppend
RunGameSubmode_TitlePasswordSub_NoApp:
	;Check for B/LEFT press
	lda JoypadDown
	and #(JOY_B|JOY_LEFT)
	beq RunGameSubmode_TitlePasswordSub_NoDel
	;Delete character
	jmp PasswordCharDelete
RunGameSubmode_TitlePasswordSub_NoDel:
	;Clear hold button timer
	lda #$00
	sta PasswordHoldTimer
	;Check to flash current character based on bit 3 of global timer
	lda LevelGlobalTimer
	and #$08
	beq PasswordCharWriteVRAMBuffer
	;Get VRAM buffer address
	jsr PasswordCharGetVRAMAddr
	;Clear current character in VRAM
	lda #$00
	sta VRAMBuffer,x
	inx
	;End VRAM buffer
	jmp WriteVRAMBufferCmd_End

PasswordCharWriteVRAMBuffer:
	;Get VRAM buffer address
	jsr PasswordCharGetVRAMAddr
	;Set current character in VRAM
	ldy PasswordIndex
	lda PasswordBuffer,y
	sta VRAMBuffer,x
	inx
	;End VRAM buffer
	jmp WriteVRAMBufferCmd_End

PasswordCharInc:
	;If bit 0 of global timer 0, exit early
	lda LevelGlobalTimer
	and #$01
	beq PasswordCharGetVRAMAddr_Exit
	;If button hold timer 0, increment timer and character
	lda PasswordHoldTimer
	beq PasswordCharInc_IncT
	;If button hold timer >= $10, increment character
	cmp #$10
	bcs PasswordCharInc_IncC
	;If button hold timer < $10, increment timer
	inc PasswordHoldTimer
	rts
PasswordCharInc_IncT:
	;Increment button held timer
	inc PasswordHoldTimer
PasswordCharInc_IncC:
	;Get VRAM buffer address
	jsr PasswordCharGetVRAMAddr
	;Increment current character
	ldy PasswordIndex
	lda PasswordBuffer,y
	clc
	adc #$01
	cmp #$2B
	bne PasswordCharInc_NoC
	lda #$01
PasswordCharInc_NoC:
	sta PasswordBuffer,y
	;Set current character in VRAM
	sta VRAMBuffer,x
	inx
	;End VRAM buffer
	jsr WriteVRAMBufferCmd_End
PasswordCharInc_Sound:
	;Play sound
	ldy #SE_CURSMOVE
	jmp LoadSound

PasswordCharGetVRAMAddr:
	;Init VRAM buffer row
	jsr WriteVRAMBufferCmd_InitRow
	;Set VRAM buffer address
	lda PasswordIndex
	clc
	adc #$94
	sta VRAMBuffer,x
	inx
	lda #$22
	sta VRAMBuffer,x
	inx
PasswordCharGetVRAMAddr_Exit:
	rts

PasswordCharDec:
	;If bit 0 of global timer 0, exit early
	lda LevelGlobalTimer
	and #$01
	beq PasswordCharGetVRAMAddr_Exit
	;If button hold timer 0, increment timer and decrement character
	lda PasswordHoldTimer
	beq PasswordCharDec_IncT
	;If button hold timer >= $10, decrement character
	cmp #$10
	bcs PasswordCharDec_DecC
	;If button hold timer < $10, increment timer
	inc PasswordHoldTimer
	rts
PasswordCharDec_IncT:
	;Increment button held timer
	inc PasswordHoldTimer
PasswordCharDec_DecC:
	;Get VRAM buffer address
	jsr PasswordCharGetVRAMAddr
	;Decrement current character
	ldy PasswordIndex
	lda PasswordBuffer,y
	sec
	sbc #$01
	bne PasswordCharInc_NoC
	lda #$2A
	bne PasswordCharInc_NoC

PasswordCharDelete:
	;Set current character in VRAM
	jsr PasswordCharWriteVRAMBuffer
	;Decrement character index
	dec PasswordIndex
	;If character index >= 0, play sound
	lda PasswordIndex
	cmp #$FF
	bne PasswordCharInc_Sound
	;Increment character index
	inc PasswordIndex
	rts

PasswordCharAppend:
	;Set current character in VRAM
	jsr PasswordCharWriteVRAMBuffer
	;Increment character index
	inc PasswordIndex
	;If character index not $05, play sound
	lda PasswordIndex
	cmp #$05
	bne PasswordCharInc_Sound
	;Decrement character index
	dec PasswordIndex
	;Check for hard mode
	ldx #$04
PasswordCharAppend_HardCheckLoop:
	;Check if character matches hard mode password
	lda PasswordBuffer,x
	cmp HardModePassword,x
	bne PasswordCharAppend_NormalCheck
	;Loop for each character
	dex
	bpl PasswordCharAppend_HardCheckLoop
	;Set hard mode flag
	lda #$01
	sta HardModeFlag
	;Set intro dialog
	jmp RunGameSubmode_TitleScreenWait_ToDialog
PasswordCharAppend_NormalCheck:
	;Decode password
	ldx #$04
PasswordCharAppend_NormalCheckLoop:
	;Decode password character
	ldy PasswordBuffer,x
	lda PasswordCharDecodeTable,y
	bmi PasswordCharAppend_Invalid
	sta PasswordDecodeBuffer,x
	;Loop for each character
	dex
	bpl PasswordCharAppend_NormalCheckLoop
	;Check if password is valid
	ldx #$04
PasswordCharAppend_ValidCheckLoop:
	;Loop for each character
	dex
	bmi PasswordCharAppend_Valid
	;Check if password character is valid
	lda PasswordDecodeBuffer,x
	and DecodeCharMaskTable,x
	cmp DecodeCharCompareTable,x
	beq PasswordCharAppend_ValidCheckLoop
PasswordCharAppend_Invalid:
	;Play sound
	ldy #SE_PASSINVALID
	jmp LoadSound
PasswordCharAppend_Valid:
	;Init game state
	jsr RunGameMode_InitGameState
	;Init character power level
	ldx #$04
PasswordCharAppend_SetPowerLoop:
	;Set character power level
	lda PasswordDecodeBuffer,x
	and #$03
	tay
	lda DecodeCharacterPowerMaxTable,y
	sta a:CharacterPowerMax,x
	;Loop for each character
	dex
	bpl PasswordCharAppend_SetPowerLoop
	;Enable character Bucky
	lda CharacterPowerMax
	ora #$80
	sta CharacterPowerMax
	;Clear levels beaten
	lda #$00
	sta $00
	;Check to set levels 1-4 as beaten
	ldx #$04
PasswordCharAppend_SetLevelsLoop:
	;If character unlocked, set corresponding level as beaten
	lda PasswordDecodeBuffer,x
	and #$03
	beq PasswordCharAppend_SetLevelsNext
	;Set level as beaten
	lda DecodeLevels1234BeatenTable-1,x
	ora $00
	sta $00
PasswordCharAppend_SetLevelsNext:
	;Loop for each level
	dex
	bne PasswordCharAppend_SetLevelsLoop
	;Check to set levels 5-7 as beaten
	lda PasswordDecodeBuffer+4
	and #$08
	sta $01
	lda PasswordDecodeBuffer+3
	and #$04
	ora $01
	lsr
	lsr
	tay
	;Set levels beaten
	lda DecodeLevels567BeatenTable,y
	ora $00
	sta LevelsCompletedFlags
	;If a level from levels 1-4 not beaten, go to stage select
	and #$0F
	cmp #$0F
	beq PasswordCharAppend_NoStageSelect
	;Next mode ($03: Stage select)
	jsr ToStageSelect
	lda #$03
	sta GameMode
	rts
PasswordCharAppend_NoStageSelect:
	;Check if level 7 is beaten
	lda LevelsCompletedFlags
	cmp #$7F
	beq PasswordCharAppend_Level7
	;Check if level 6 is beaten
	cmp #$3F
	beq PasswordCharAppend_Level6
	;Check if level 5 is beaten
	cmp #$1F
	beq PasswordCharAppend_Level5
	;Disable all characters except Bucky and Blinky
	lda CharacterPowerMax+1
	and #$7F
	sta CharacterPowerMax+1
	lda CharacterPowerMax+2
	and #$7F
	sta CharacterPowerMax+2
	lda CharacterPowerMax+4
	and #$7F
	sta CharacterPowerMax+4
	;Set captured dialog
	lda #$0B
	bne PasswordCharAppend_SetDialog
PasswordCharAppend_Level5:
	;Set level 5 beaten dialog
	lda #$0C
	bne PasswordCharAppend_SetDialog
PasswordCharAppend_Level6:
	;Set level 6 beaten dialog
	lda #$0D
	bne PasswordCharAppend_SetDialog
PasswordCharAppend_Level7:
	;Set level 7 beaten dialog
	lda #$0E
PasswordCharAppend_SetDialog:
	sta DialogID
	;Next mode ($02: Dialog)
	lda #$02
	sta GameMode
	;Next submode ($00: Init)
	lda #$00
	sta GameSubmode
	rts
