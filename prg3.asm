	;If scroll X position $81 or $C0 or $FF, shoot projectile
	lda TempIRQBufferScrollX
	cmp #$FF
	beq Enemy81Sub_Sub1_Shoot
	cmp #$C0
	beq Enemy81Sub_Sub1_Shoot
	cmp #$81
	bne Enemy81Sub_Sub1_NoShoot
