	;Spawn enemy
	lda #$00
	sta $00
	lda #$22
	sta $01
	ldy #$02
	lda #$A1
	jsr SpawnEnemy
	;Set enemy HP
	lda #$A0
	sta Enemy_HP+$02
	;Next task ($0D: Bottom part)
	lda #$0D
	sta Enemy_Temp0+$02
	;Set enemy ID to level 6 boss
	lda #ENEMY_LEVEL6BOSS
	sta Enemy_ID+$02
	;Set boss enemy slot index
	lda #$01
	sta BossEnemyIndex
