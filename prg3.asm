	.dw Enemy6ASub_Sub0	;$00  Init
	.dw Enemy6ASub_Sub1	;$01  Offscreen
	.dw Enemy6ASub_Sub2	;$02  Fall part 1
	.dw Enemy6ASub_Sub3	;$03  Fall part 2
	.dw Enemy6ASub_Sub4	;$04  Shooting
	.dw Enemy6ASub_Sub5	;$05  Idle
	.dw Enemy6ASub_Sub6	;$06  Down part 1
	.dw Enemy6ASub_Sub7	;$07  Down part 2
	.dw Enemy6ASub_Sub8	;$08  Lasers
	.dw Enemy6ASub_Sub8	;$09  Lasers end
	.dw Enemy6ASub_SubA	;$0A  Up part 1
	.dw Enemy6ASub_SubB	;$0B  Up part 2
	.dw Enemy6ASub_SubC	;$0C  Jump
	.dw Enemy6ASub_SubD	;$0D  Bottom part
;$00: Init
