	clc
	adc #$04
	sta TempIRQBufferHeight
	lda TempIRQBufferHeight+1
	sec
	sbc #$04
	sta TempIRQBufferHeight+1
	;If robot hit ground, setup next task ($04: Shooting)
	cmp #$FF
	beq Enemy6ASub_Sub3_Next
	;If height of section below robot $77, set enemy Y velocity
	cmp #$77
	bne Enemy6ASub_Sub3_Exit
	;Set enemy Y velocity
	lda #$04
