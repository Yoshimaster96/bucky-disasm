	;Next task ($01: Offscreen)
	lda #$01
	sta Enemy_Temp0+$01
	;Set scroll Y position
	lda #$BE
	sta TempMirror_PPUSCROLL_Y
	;Init animation timer
	lda #$40
	sta Enemy_Temp1+$01
