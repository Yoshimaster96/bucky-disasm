	;Decrement animation timer, check if 0
	dec Enemy_Temp1+$01
	bne Enemy6ASub_SubD
	;Reset animation timer
	lda #$40
	sta Enemy_Temp1+$01
	;Next task ($02: Fall part 1)
	inc Enemy_Temp0+$01
	;Play sound
	ldy #SE_L6FALL
	jmp LoadSound
