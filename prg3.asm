	;If scroll X position $E0, set enemy sprite down
	lda TempIRQBufferScrollX
	cmp #$E0
	bcs Enemy81Sub_Sub1_Down
	;If scroll X position $A0, set enemy sprite up
	cmp #$A0
	bcc Enemy81Sub_Sub1_Up
	;Set enemy sprite left
	lda #$E8
	bne Enemy81Sub_Sub1_SetSprite
Enemy81Sub_Sub1_Up:
	;Set enemy sprite up
	lda #$EA
	bne Enemy81Sub_Sub1_SetSprite
