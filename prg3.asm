Enemy6ASub:
	;Check if already inited
	lda Enemy_Temp0+$01
	beq Enemy6ASub_JT
	;If bottom part not active, clear enemy invincible flag
	lda Enemy_Flags+$02
	bne Enemy6ASub_JT
	;Clear enemy invincible flag
	lda Enemy_Flags+$01
	and #~EF_INVINCIBLE
	sta Enemy_Flags+$01
Enemy6ASub_JT:
	;Do jump table
	lda Enemy_Temp0,x
	jsr DoJumpTable
